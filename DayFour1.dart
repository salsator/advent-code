void main() {
  int result = 0;
  int maxWidth = 0; //139
  int maxHeight = 0; //139
  final lines = data.split('\n');
  for (var line in data.split('\n')) {
    maxWidth = line.length;
    maxHeight += 1;
  }
  for (int i = 0; i < maxHeight; i++) {
    for (int j = 0; j < maxWidth; j++) {
      // procházám každý znak
      lines[i][j];
      int value = 0;
      //print("$i $j");
      int minK = i - 1;
      int maxK = i + 1;
      int minL = j - 1;
      int maxL = j + 1;

      if (i == 0) {
        minK = i;
      }
      if (i == maxHeight - 1) {
        maxK = i;
      }
      if (j == 0) {
        minL = j;
      }
      if (j == maxWidth - 1) {
        maxL = j;
      }

      if (lines[i][j] != '@') {
        continue;
      }

      for (int k = minK; k <= maxK; k++) {
        for (int l = minL; l <= maxL; l++) {
          if (k == i && l == j) {
            continue;
          }
          if (lines[k][l] == '@') {
            value += 1;
          }
        }
      }
      if (value < 4) {
        result += 1;
      }
    }
  }
  print(result);
}

